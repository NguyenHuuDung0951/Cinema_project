    public void MovieCard(String title,ImageIcon poster, String[] showtimes){
        lbltitle.setText(title);

        Image img = poster.getImage().getScaledInstance(250, 280, Image.SCALE_SMOOTH);
        lblPoster.setIcon(new ImageIcon(img));

        jPanel2.removeAll();
        jPanel2.setLayout(new java.awt.FlowLayout(java.awt.FlowLayout.CENTER, 5, 5));

    for (String time : showtimes) {
        javax.swing.JButton btnTime = new javax.swing.JButton(time);
        btnTime.setFocusPainted(false);
        jPanel2.add(btnTime);
    }

    jPanel2.revalidate();
    jPanel2.repaint();
    }
    private void addCustomButtons() {
    jPanel3.removeAll(); // xóa nút cũ nếu có

    ButtonMenu btnTrailer = new ButtonMenu();
    ButtonMenu btnChiTiet = new ButtonMenu();
    btnTrailer.setText("Trailer");
    btnChiTiet.setText("Chi Tiết");
    
    btnTrailer.addActionListener(e -> {
        JOptionPane.showMessageDialog(this, "Trailer sẽ mở ở đây!");
    });

    btnChiTiet.addActionListener(e -> {
        JOptionPane.showMessageDialog(this, "Chi tiết phim sẽ hiển thị!");
    });

    jPanel3.setLayout(new java.awt.FlowLayout(java.awt.FlowLayout.CENTER, 30, 10));
    jPanel3.add(btnTrailer);
    jPanel3.add(btnChiTiet);

    jPanel3.revalidate();
    jPanel3.repaint();
}
